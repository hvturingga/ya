package main

import (
	"encoding/json"
	"fmt"
	"github.com/hvturingga/ya/conf"
	"os"
	"text/template"
)

const providerTpl = `// Code generated by go generate; DO NOT EDIT.
package provider

import "github.com/hvturingga/ya/conf"

func GetProviderList() []conf.Provider {
	return []conf.Provider{
{{- range . }}
		{Repo: "{{ .Repo }}", Owner: "{{ .Owner }}", Version: "{{ .Version }}"},
{{- end }}
	}
}
`

func main() {

	wd, err := os.Getwd()
	if err != nil {
		panic(err)
	}
	f, err := os.ReadFile(fmt.Sprintf("%s/conf/provider.json", wd))
	if err != nil {
		panic(err)
	}

	var providers []conf.Provider
	if err := json.Unmarshal(f, &providers); err != nil {
		panic(err)
	}

	tmpl, err := template.New("providers").Parse(providerTpl)
	if err != nil {
		panic(err)
	}

	create, err := os.Create(fmt.Sprintf("%s/internal/provider/provider_list.go", wd))
	if err != nil {
		panic(err)
	}
	defer create.Close()

	if err := tmpl.Execute(create, providers); err != nil {
		panic(err)
	}
}
